-- Create 'produits' table
CREATE TABLE IF NOT EXISTS produits (
    id SERIAL PRIMARY KEY,
    nom VARCHAR(255) NOT NULL,
    description TEXT NOT NULL,
    prix DECIMAL(10, 2) NOT NULL,
    image_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Create 'commandes' table
CREATE TABLE IF NOT EXISTS commandes (
    id SERIAL PRIMARY KEY,
    nom_client VARCHAR(255) NOT NULL,
    telephone VARCHAR(50) NOT NULL,
    adresse TEXT NOT NULL,
    details_produits TEXT NOT NULL, -- JSON string or comma-separated list of items
    statut VARCHAR(50) DEFAULT 'En attente' NOT NULL, -- 'En attente', 'Confirmé', 'En livraison', 'Livré', 'Annulé'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

